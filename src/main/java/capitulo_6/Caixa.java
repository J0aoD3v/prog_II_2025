package capitulo_6;

public class Caixa {

    private int id;
    private String conteudo;

    public Caixa() {
        conteudo = "";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getConteudo() {
        return conteudo;
    }

    public void setConteudo(String conteudo) {
        this.conteudo = conteudo;
    }
}
